    public function meta_init() {

	// add a meta box for each of the wordpress page types: posts and pages
	foreach (array('post','page') as $type) {
	    add_meta_box(
		"tieroom_".$type."_meta",
		'Tieroom Integration',
		array($this, 'meta_setup'),
		$type,
		'normal',
		'high'
	    );
	}

	add_action('save_post',array($this, 'meta_save'));
    }

    public function meta_setup() {
	global $post;

	// using an underscore, prevents the meta variable
	// from showing up in the custom fields section
	$meta = get_post_meta($post->ID,'_tieroom',TRUE);

	// instead of writing HTML here, lets do an include
	include(dirname(__FILE__) . '/views/public.php');

	// create a custom nonce for submit verification later
	echo '<input type="hidden" name="tieroom_wp_magento_nonce" value="' . wp_create_nonce(__FILE__) . '" />';
    }

    public function meta_save($post_id) {
	// authentication checks

	// make sure data came from our meta box
	if (!wp_verify_nonce($_POST['tieroom_wp_magento_nonce'],__FILE__)) return $post_id;

	// check user permissions
	if ($_POST['post_type'] == 'page') {
	    if (!current_user_can('edit_page', $post_id)) {
		return $post_id;
	    }
	} else {
	    if (!current_user_can('edit_post', $post_id)) {
		return $post_id;
	    }
	}


	$current_data = get_post_meta($post_id, '_tieroom', TRUE);
	$new_data = $_POST['_tieroom'];

	if ($current_data) {

	    if (is_null($new_data)) {
		delete_post_meta($post_id, '_tieroom');
	    } else {
		update_post_meta($post_id, '_tieroom', $new_data);
	    }

	} else if (!is_null($new_data)) {
	    add_post_meta($post_id, '_tieroom', $new_data, TRUE);
	}

	return $post_id;

	//OK, looks good, save it
    }

